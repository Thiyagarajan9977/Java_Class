package org.control_Statements;

public class NestedIf {

	public static void main(String[] args) {

		// using AND Operator
		
		int age = 20;

		int weight = 59;

		if (age >= 18 && weight >= 60) {

			System.out.println("Able to donate");

		} else {
			System.out.println("Can't donate");
		}

	}

}
