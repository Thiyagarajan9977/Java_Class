package org.control_Statements;

public class Incrementing {
		
		public static void main(String args[]){  
		int x=10;  
		
		
		System.out.println(x++);//10 (11) - First Print the x Value as 10 and then increment
		
		System.out.println(++x);//12 - Here, Already know value incremented by 1 =11, now add
										//the value and print
		
		System.out.println(x--);//12 (11) -  Here, Already know value Decrement by 1 =11, now add
                         		// the value and print
		
		System.out.println(--x);//10
		
		}
}


