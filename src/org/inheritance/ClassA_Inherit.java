package org.inheritance;


public class ClassA_Inherit   {
	
	public void class1() {
		System.out.println("Student");

	}
	
	public void class2() {
		System.out.println("Marks");

	}
	
	public void class3() {
		System.out.println("Subject");

	}
	
public static void main(String[] args) {
		
		ClassA_Inherit c = new ClassA_Inherit();
		c.class1();
		c.class2();
		c.class3();
				
		
	}


}
